"use client";

import {
  createContext,
  useContext,
  useState,
  useEffect,
  type ReactNode,
} from "react";
import { supabase } from "@/lib/supabase";
import type { User as SupabaseUser } from "@supabase/supabase-js";

interface User {
  id: string;
  name: string;
  email: string;
  avatar?: string;
  bio?: string;
  phone?: string;
  birth_date?: string;
  gender?: string;
  address?: string;
  city?: string;
  country?: string;
  occupation?: string;
  learning_goal?: string;
  language_level?: string;
  user_metadata?: {
    full_name?: string;
    avatar_url?: string;
    bio?: string;
    phone?: string;
    birth_date?: string;
    gender?: string;
    address?: string;
    city?: string;
    country?: string;
    occupation?: string;
    learning_goal?: string;
    language_level?: string;
  };
}

interface AuthContextType {
  user: User | null;
  loading: boolean;
  signIn: (
    email: string,
    password: string
  ) => Promise<{ error?: { message: string } }>;
  signUp: (
    email: string,
    password: string,
    options?: { data: { full_name: string } }
  ) => Promise<{ error?: { message: string } }>;
  signOut: () => Promise<void>;
  updateProfile: (data: {
    name?: string;
    avatar_url?: string;
    bio?: string;
    phone?: string;
    birth_date?: string;
    gender?: string;
    address?: string;
    city?: string;
    country?: string;
    occupation?: string;
    learning_goal?: string;
    language_level?: string;
  }) => Promise<void>;
  loginWithGoogle: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function AuthProvider({ children }: { children: ReactNode }) {
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    // Get initial session t·ª´ Supabase TH·∫¨T
    const getInitialSession = async () => {
      const {
        data: { session },
      } = await supabase.auth.getSession();
      if (session?.user) {
        setUser(mapSupabaseUser(session.user));
      }
      setLoading(false);
    };

    getInitialSession();

    // Listen for auth state changes TH·∫¨T
    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange(async (event, session) => {
      if (session?.user) {
        setUser(mapSupabaseUser(session.user));
      } else {
        setUser(null);
      }
      setLoading(false);
    });

    return () => subscription.unsubscribe();
  }, []);

  const mapSupabaseUser = (supabaseUser: SupabaseUser): User => {
    return {
      id: supabaseUser.id,
      name:
        supabaseUser.user_metadata?.name ||
        supabaseUser.user_metadata?.full_name ||
        "Ng∆∞·ªùi d√πng",
      email: supabaseUser.email || "",
      avatar:
        supabaseUser.user_metadata?.avatar_url ||
        generateAvatarPlaceholder(
          supabaseUser.user_metadata?.name || supabaseUser.email || "U"
        ),
      bio: supabaseUser.user_metadata?.bio,
      phone: supabaseUser.user_metadata?.phone,
      birth_date: supabaseUser.user_metadata?.birth_date,
      gender: supabaseUser.user_metadata?.gender,
      address: supabaseUser.user_metadata?.address,
      city: supabaseUser.user_metadata?.city,
      country: supabaseUser.user_metadata?.country,
      occupation: supabaseUser.user_metadata?.occupation,
      learning_goal: supabaseUser.user_metadata?.learning_goal,
      language_level: supabaseUser.user_metadata?.language_level,
      user_metadata: {
        full_name:
          supabaseUser.user_metadata?.full_name ||
          supabaseUser.user_metadata?.name,
        avatar_url: supabaseUser.user_metadata?.avatar_url,
        bio: supabaseUser.user_metadata?.bio,
        phone: supabaseUser.user_metadata?.phone,
        birth_date: supabaseUser.user_metadata?.birth_date,
        gender: supabaseUser.user_metadata?.gender,
        address: supabaseUser.user_metadata?.address,
        city: supabaseUser.user_metadata?.city,
        country: supabaseUser.user_metadata?.country,
        occupation: supabaseUser.user_metadata?.occupation,
        learning_goal: supabaseUser.user_metadata?.learning_goal,
        language_level: supabaseUser.user_metadata?.language_level,
      },
    };
  };

  const signIn = async (email: string, password: string) => {
    setLoading(true);
    try {
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });

      if (error) {
        // Map Supabase login errors to user-friendly Vietnamese messages
        let errorMessage = error.message;

        if (error.message.includes("Invalid login credentials")) {
          errorMessage = "Email ho·∫∑c m·∫≠t kh·∫©u kh√¥ng ƒë√∫ng";
        } else if (error.message.includes("Email not confirmed")) {
          errorMessage =
            "Email ch∆∞a ƒë∆∞·ª£c x√°c nh·∫≠n. Vui l√≤ng ki·ªÉm tra h·ªôp th∆∞ v√† x√°c nh·∫≠n email tr∆∞·ªõc khi ƒëƒÉng nh·∫≠p";
        } else if (error.message.includes("Too many requests")) {
          errorMessage = "Qu√° nhi·ªÅu l·∫ßn th·ª≠ ƒëƒÉng nh·∫≠p. Vui l√≤ng ƒë·ª£i v√†i ph√∫t";
        } else if (error.message.includes("User not found")) {
          errorMessage =
            "T√†i kho·∫£n kh√¥ng t·ªìn t·∫°i. Vui l√≤ng ƒëƒÉng k√Ω t√†i kho·∫£n m·ªõi";
        } else if (error.message.includes("Invalid email")) {
          errorMessage = "ƒê·ªãnh d·∫°ng email kh√¥ng h·ª£p l·ªá";
        }

        return { error: { message: errorMessage } };
      }

      if (data.user) {
        setUser(mapSupabaseUser(data.user));
      }
      return {};
    } catch (error: any) {
      return { error: { message: error.message || "ƒêƒÉng nh·∫≠p th·∫•t b·∫°i" } };
    } finally {
      setLoading(false);
    }
  };

  const signUp = async (
    email: string,
    password: string,
    options?: { data: { full_name: string } }
  ) => {
    setLoading(true);
    try {
      // Dynamic redirect URL detection
      const redirectUrl =
        typeof window !== "undefined"
          ? `${window.location.origin}/auth/callback`
          : `${
              process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"
            }/auth/callback`;

      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          emailRedirectTo: redirectUrl,
          data: {
            name: options?.data?.full_name,
            full_name: options?.data?.full_name,
          },
        },
      });

      if (error) {
        // Map Supabase errors to user-friendly Vietnamese messages
        let errorMessage = error.message;

        if (error.message.includes("User already registered")) {
          errorMessage = "Email n√†y ƒë√£ ƒë∆∞·ª£c ƒëƒÉng k√Ω";
        } else if (error.message.includes("already registered")) {
          errorMessage = "T√†i kho·∫£n v·ªõi email n√†y ƒë√£ t·ªìn t·∫°i";
        } else if (error.message.includes("Email rate limit")) {
          errorMessage = "Qu√° nhi·ªÅu y√™u c·∫ßu, vui l√≤ng ƒë·ª£i v√†i ph√∫t";
        } else if (error.message.includes("Invalid email")) {
          errorMessage = "ƒê·ªãa ch·ªâ email kh√¥ng h·ª£p l·ªá";
        } else if (error.message.includes("Password should be at least")) {
          errorMessage = "M·∫≠t kh·∫©u ph·∫£i c√≥ √≠t nh·∫•t 6 k√Ω t·ª±";
        } else if (error.message.includes("signup is disabled")) {
          errorMessage = "T√≠nh nƒÉng ƒëƒÉng k√Ω t·∫°m th·ªùi b·ªã v√¥ hi·ªáu h√≥a";
        }

        return { error: { message: errorMessage } };
      }

      // KH√îNG t·ª± ƒë·ªông set user - ƒë·ªÉ user x√°c th·ª±c email tr∆∞·ªõc
      return {};
    } catch (error: any) {
      return { error: { message: error.message || "ƒêƒÉng k√Ω th·∫•t b·∫°i" } };
    } finally {
      setLoading(false);
    }
  };

  const loginWithGoogle = async () => {
    setLoading(true);
    try {
      console.log("üîµ Attempting Google OAuth login...");
      console.log("üîµ Supabase URL:", process.env.NEXT_PUBLIC_SUPABASE_URL);
      console.log("üîµ Current origin:", window.location.origin);

      // Ki·ªÉm tra xem c√≥ Supabase URL kh√¥ng
      if (!process.env.NEXT_PUBLIC_SUPABASE_URL) {
        throw new Error(
          "‚ùå NEXT_PUBLIC_SUPABASE_URL ch∆∞a ƒë∆∞·ª£c c·∫•u h√¨nh trong .env.local"
        );
      }

      if (!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY) {
        throw new Error(
          "‚ùå NEXT_PUBLIC_SUPABASE_ANON_KEY ch∆∞a ƒë∆∞·ª£c c·∫•u h√¨nh trong .env.local"
        );
      }

      console.log("‚úÖ Environment variables OK");

      // Test k·∫øt n·ªëi Supabase tr∆∞·ªõc
      const { data: testSession, error: sessionError } =
        await supabase.auth.getSession();
      if (sessionError) {
        console.error("üî¥ Supabase connection failed:", sessionError);
        throw new Error(`Kh√¥ng th·ªÉ k·∫øt n·ªëi Supabase: ${sessionError.message}`);
      }
      console.log("‚úÖ Supabase connection OK");

      // Th·ª≠ Google OAuth
      const redirectUrl =
        typeof window !== "undefined"
          ? `${window.location.origin}/auth/callback`
          : `${
              process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"
            }/auth/callback`;

      const { data, error } = await supabase.auth.signInWithOAuth({
        provider: "google",
        options: {
          redirectTo: redirectUrl,
          queryParams: {
            access_type: "offline",
            prompt: "consent",
          },
        },
      });

      if (error) {
        console.error("üî¥ Supabase Google OAuth error:", error);
        console.error("üî¥ Error details:", JSON.stringify(error, null, 2));

        // Specific error messages based on common issues
        if (
          error.message.includes("Provider not found") ||
          error.message.includes("google")
        ) {
          throw new Error(
            "‚ùå Google provider ch∆∞a ƒë∆∞·ª£c k√≠ch ho·∫°t ho·∫∑c c·∫•u h√¨nh sai trong Supabase Dashboard.\n\nüîß H∆∞·ªõng d·∫´n:\n1. V√†o Supabase Dashboard > Authentication > Providers\n2. Enable Google provider\n3. Th√™m Client ID v√† Client Secret t·ª´ Google Cloud Console"
          );
        } else if (
          error.message.includes("Invalid redirect URL") ||
          error.message.includes("redirect")
        ) {
          throw new Error(
            `‚ùå Redirect URL kh√¥ng h·ª£p l·ªá.\n\nüîß C·∫ßn th√™m v√†o Google Cloud Console:\n‚Ä¢ ${window.location.origin}/auth/callback\n‚Ä¢ https://your-project-id.supabase.co/auth/v1/callback`
          );
        } else if (
          error.message.includes("unauthorized") ||
          error.message.includes("client")
        ) {
          throw new Error(
            "‚ùå Google Client ID ho·∫∑c Client Secret kh√¥ng ƒë√∫ng. Vui l√≤ng ki·ªÉm tra l·∫°i trong Supabase settings."
          );
        } else {
          throw new Error(
            `‚ùå Google OAuth error: ${
              error.message
            }\n\nüîç Chi ti·∫øt: ${JSON.stringify(error, null, 2)}`
          );
        }
      }

      console.log("üü¢ Google OAuth redirect initiated successfully");
      console.log("üü¢ OAuth data:", data);

      // OAuth redirect s·∫Ω handle vi·ªác authentication
      // Kh√¥ng c·∫ßn set loading = false ·ªü ƒë√¢y v√¨ s·∫Ω redirect
    } catch (error: any) {
      console.error("üî¥ Google login failed:", error);
      setLoading(false);
      throw new Error(
        error.message ||
          "‚ùå ƒêƒÉng nh·∫≠p Google th·∫•t b·∫°i. Vui l√≤ng ki·ªÉm tra c·∫•u h√¨nh Supabase."
      );
    }
  };

  const signOut = async () => {
    await supabase.auth.signOut();
    setUser(null);
  };

  const updateProfile = async (data: {
    name?: string;
    avatar_url?: string;
    bio?: string;
    phone?: string;
    birth_date?: string;
    gender?: string;
    address?: string;
    city?: string;
    country?: string;
    occupation?: string;
    learning_goal?: string;
    language_level?: string;
  }) => {
    if (!user) return;

    const { error } = await supabase.auth.updateUser({
      data: {
        name: data.name,
        full_name: data.name,
        avatar_url: data.avatar_url,
        bio: data.bio,
        phone: data.phone,
        birth_date: data.birth_date,
        gender: data.gender,
        address: data.address,
        city: data.city,
        country: data.country,
        occupation: data.occupation,
        learning_goal: data.learning_goal,
        language_level: data.language_level,
      },
    });

    if (error) {
      throw new Error(error.message);
    }

    const updatedUser = {
      ...user,
      name: data.name || user.name,
      bio: data.bio || user.bio,
      phone: data.phone || user.phone,
      birth_date: data.birth_date || user.birth_date,
      gender: data.gender || user.gender,
      address: data.address || user.address,
      city: data.city || user.city,
      country: data.country || user.country,
      occupation: data.occupation || user.occupation,
      learning_goal: data.learning_goal || user.learning_goal,
      language_level: data.language_level || user.language_level,
      user_metadata: {
        ...user.user_metadata,
        full_name: data.name || user.user_metadata?.full_name,
        avatar_url: data.avatar_url || user.user_metadata?.avatar_url,
        bio: data.bio || user.user_metadata?.bio,
        phone: data.phone || user.user_metadata?.phone,
        birth_date: data.birth_date || user.user_metadata?.birth_date,
        gender: data.gender || user.user_metadata?.gender,
        address: data.address || user.user_metadata?.address,
        city: data.city || user.user_metadata?.city,
        country: data.country || user.user_metadata?.country,
        occupation: data.occupation || user.user_metadata?.occupation,
        learning_goal: data.learning_goal || user.user_metadata?.learning_goal,
        language_level: data.language_level || user.user_metadata?.language_level,
      },
    };
    setUser(updatedUser);
  };

  function generateAvatarPlaceholder(name: string): string {
    const initials = name
      .trim()
      .split(/\s+/)
      .slice(0, 2)
      .map((p) => p[0]?.toUpperCase() || "U")
      .join("");
    const colors = [
      "#6366F1",
      "#0EA5E9",
      "#10B981",
      "#F59E0B",
      "#EC4899",
      "#8B5CF6",
      "#14B8A6",
    ];
    const hash = initials.charCodeAt(0) + (initials.charCodeAt(1) || 0);
    const bg = colors[hash % colors.length];
    const svg = `<svg xmlns='http://www.w3.org/2000/svg' width='128' height='128'>
      <rect width='128' height='128' rx='16' fill='${bg}' />
      <text x='50%' y='50%' dominant-baseline='middle' text-anchor='middle' font-family='Inter,Arial' font-size='56' fill='white' font-weight='600'>${initials}</text>
    </svg>`;
    return `data:image/svg+xml;base64,${btoa(
      unescape(encodeURIComponent(svg))
    )}`;
  }

  return (
    <AuthContext.Provider
      value={{
        user,
        loading,
        signIn,
        signUp,
        signOut,
        updateProfile,
        loginWithGoogle,
      }}
    >
      {children}
    </AuthContext.Provider>
  );
}

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error("useAuth must be used within an AuthProvider");
  }
  return context;
}
